    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        String nome = txtNome.getText().trim();
        String marca = txtMarca.getText().trim();
        int voltagem;
        double preco;
        
        try {
            voltagem = Integer.parseInt(txtVoltagem.getText().trim());
            preco = Double.parseDouble(txtPreco.getText().trim());
            
            if (!nome.isEmpty() && !marca.isEmpty()) {
                controller.cadastrar(nome, marca, voltagem, preco);
                JOptionPane.showMessageDialog(null, "Produto Cadastrado com sucesso!");
            }
            
            txtNome.setText("");
            txtMarca.setText("");
            txtBuscar.setText("");
            txtVoltagem.setText("");
            txtPreco.setText("");
        
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite números validos");
        }
        
        
    }//GEN-LAST:event_CadastrarActionPerformed

