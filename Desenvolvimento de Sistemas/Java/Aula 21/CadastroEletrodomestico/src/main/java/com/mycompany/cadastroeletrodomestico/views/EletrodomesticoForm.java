    private void RemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoverActionPerformed
        int linhaSelecionada = tbLista.getSelectedRow();
        
        if (linhaSelecionada != -1) {
            int id = (int) tbLista.getValueAt(linhaSelecionada, 0);
            controller.remover(id);
            atualizarTabela();
            
            txtNome.setText("");
            txtMarca.setText("");
            txtVoltagem.setText("");
            txtPreco.setText("");
            txtBuscar.setText("");
            
            JOptionPane.showMessageDialog(null, "Eletrodomestico Removido com sucesso");
            txtNome.setText("");
            txtMarca.setText("");
            txtBuscar.setText("");
            txtVoltagem.setText("");
            txtPreco.setText("");
        
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um eletrodomestico para remover");
        }
        
    }//GEN-LAST:event_RemoverActionPerformed

    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed

        if (idSelecionado == -1) {
            JOptionPane.showMessageDialog(null, "Clique em cima do eletrodomestico que deseja alterar");
            return;
         }
        
        String nome = txtNome.getText().trim();
        String marca = txtMarca.getText().trim();
        int voltagem;
        double preco;
        
        try {
            voltagem = Integer.parseInt(txtVoltagem.getText().trim());
            preco = Double.parseDouble(txtPreco.getText().trim());
            
            if (!nome.isEmpty() && !marca.isEmpty()) {
                controller.atualizar(idSelecionado,nome, marca, voltagem, preco);
                JOptionPane.showMessageDialog(null, "Produto Atualizado com sucesso!");
            }
            
            txtNome.setText("");
            txtMarca.setText("");
            txtBuscar.setText("");
            txtVoltagem.setText("");
            txtPreco.setText("");
        
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite números validos");
        }

    }//GEN-LAST:event_AlterarActionPerformed

