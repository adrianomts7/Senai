    private void atualizarTabela() {
        tabelaModelo.setRowCount(0);
        
        ArrayList<Eletrodomestico> lista = controller.listar();
        
        for (Eletrodomestico e : lista) {
            tabelaModelo.addRow(new Object[]{ e.getId() ,e.getNome(), e.getMarca(), e.getVoltagem(), e.getPreco() });
        }
        
    }
    
    private void buscarDinamicamente() {
        String nomeBuscado = txtBuscar.getText().trim();
        
        tabelaModelo.setRowCount(0);
        ArrayList<Eletrodomestico> lista = controller.buscarPorNome(nomeBuscado);
        
        for (Eletrodomestico e : lista) {
            tabelaModelo.addRow(new Object[]{e.getId() ,e.getNome(), e.getMarca(), e.getVoltagem(), e.getPreco() });
        }
        
    }
    
    public void limparTela() {
        txtNome.setText("");
        txtMarca.setText("");
        txtBuscar.setText("");
        txtVoltagem.setText("");
        txtPreco.setText("");
        
        idSelecionado = -1;
        
        tabelaModelo.setRowCount(0);
    }
    
