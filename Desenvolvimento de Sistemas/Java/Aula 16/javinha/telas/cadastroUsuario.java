    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        String nomeBuscar = txtBuscar.getText().trim();
        
        if (nomeBuscar.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite um nome para fazer a busca!");
        }
        
        boolean encontrado = false;
        
        for(Pessoa p : listaPessoas) {
            if(p.getNome().equalsIgnoreCase(nomeBuscar)) {
                txtNome.setText(p.getNome());
                txtIdade.setText(String.valueOf(p.getIdade()));
                encontrado = true;
                break;
            }
        }
        
        if (!encontrado) {
            JOptionPane.showMessageDialog(null, "Pessoa não encontrada");
            txtNome.setText("");
            txtIdade.setText("");
        }
        
        
        
    }//GEN-LAST:event_buscarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        String nomeBuscar = txtBuscar.getText().trim();
        
        if (nomeBuscar.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite o nome para remover");
            return;
        }
        
        boolean removido = false;
        
        for(int i = 0; i < listaPessoas.size(); i++) {
            if(listaPessoas.get(i).getNome().equalsIgnoreCase(nomeBuscar)) {
                listaPessoas.remove(i);
                removido = true;
                break;
            }
        }
        
        if (removido) {
            JOptionPane.showMessageDialog(null, "Pessoa removida com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Pessoa não encontrado!");
        }
        
    }//GEN-LAST:event_btnRemoverActionPerformed

