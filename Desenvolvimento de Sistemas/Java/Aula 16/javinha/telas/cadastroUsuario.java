    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        String nomeBusca = txtBuscar.getText().trim();
        String novoNome = txtNome.getText().trim();
        String novaIdadeTexto = txtIdade.getText().trim();
        
        if (nomeBusca.isEmpty() || novoNome.isEmpty() || novaIdadeTexto.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Prencha todos os campos");
            return;
        }
        
        try {
            int novaIdade = Integer.parseInt(novaIdadeTexto);
            boolean encontrado = false;
            
            for(Pessoa p : listaPessoas) {
                if(p.getNome().equalsIgnoreCase(nomeBusca)) {
                    p.setNome(novoNome);
                    p.setIdade(novaIdade);
                    encontrado = true;
                    break;
                }
            }
            
            if (encontrado) {
                JOptionPane.showMessageDialog(null, "Dados atualizados com sucesso!");
                txArea.setText("");
                for(Pessoa p2 : listaPessoas) {
                    txArea.append("Nome: " + p2.getNome() + "\nIdade: " + p2.getIdade() + " Anos"  + "\n");
                    txArea.append("\n");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Pessoa não encontrada");
            }
            
        } catch (NumberFormatException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Idade invalida");
        }
        
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        try {
            String nome = txtNome.getText();
            int idade = Integer.parseInt(txtIdade.getText());
            
            Pessoa p = new Pessoa(nome, idade);
            listaPessoas.add(p);
            txArea.append("Nome: " + p.getNome() + "\nIdade: " + p.getIdade()+ " Anos" + "\n");
            txArea.append("\n");
            
            txtNome.setText("");
            txtIdade.setText("");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Idade invalida");
        }
    }//GEN-LAST:event_cadastrarActionPerformed

