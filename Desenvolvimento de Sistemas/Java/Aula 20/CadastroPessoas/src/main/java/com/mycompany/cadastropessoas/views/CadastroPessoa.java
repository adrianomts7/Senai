    private void RemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoverActionPerformed
        int linhaSelecionada = tbLista.getSelectedRow();
        
        if (linhaSelecionada != -1) {
            int id = (int) tbLista.getValueAt(linhaSelecionada, 0);
            controller.remover(id);
            atualizarTabela();
            
            txtNome.setText("");
            txtIdade.setText("");
            
            JOptionPane.showMessageDialog(null, "Pessoa removida com sucesso");
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma pessoa para remover");
        }
        
    }//GEN-LAST:event_RemoverActionPerformed

    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        String nome = txtNome.getText().trim();
        int idade;
        
        try {
            idade = Integer.parseInt(txtIdade.getText().trim());
            
            if (!nome.isEmpty()) {
                if(!modoAtualizado) {
                    controller.cadastrar(nome, idade);
                    JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!"); 
                } else {
                    controller.atualizar(idSelecionado, nome, idade);
                    JOptionPane.showMessageDialog(null, "Atualização realizada com sucesso");
                    Cadastrar.setText("cadastrar");
                    idSelecionado = -1;
                    modoAtualizado = false;
                }
                
                atualizarTabela();
                txtNome.setText("");
                txtIdade.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "O nome não pode ficar vazio");
            }
            
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Idade deve ser um número inteiro");
        }
            
        
    }//GEN-LAST:event_CadastrarActionPerformed

