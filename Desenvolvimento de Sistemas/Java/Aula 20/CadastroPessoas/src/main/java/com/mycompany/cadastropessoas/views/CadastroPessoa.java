    public CadastroPessoa() {
        initComponents();
        tabelaModelo = (DefaultTableModel) tbLista.getModel();
        atualizarTabela();
        
        txtBuscar.addKeyListener(new java.awt.event.KeyAdapter(){
            public void keyReleased (java.awt.event.KeyEvent evt) {
                buscarDinamicamente();
            }
        });
        
        tbLista.getSelectionModel().addListSelectionListener(e -> {
            // Garante que so clique na tabale so uma vez
            if(!e.getValueIsAdjusting()) {
                int linhaSelecionada = tbLista.getSelectedRow();
                
                // Verifica se há uma linha selecionada
                if (linhaSelecionada != -1) {
                    // Pegando o id da pessoa onde clicou na tabela
                    idSelecionado = (int) tbLista.getValueAt(linhaSelecionada, 0);
                    
                    
                    txtNome.setText((String) tbLista.getValueAt(linhaSelecionada, 1));

                    txtIdade.setText(String.valueOf(tbLista.getValueAt(linhaSelecionada, 2)));
                    
                    modoAtualizado = true;
                    Cadastrar.setText("Atualizar");
                }
            }
        });
        
        this.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mousePressed(java.awt.event.MouseEvent evt) {
                if(!tbLista.getBounds().contains(evt.getPoint())) {
                    tbLista.clearSelection();
                    idSelecionado = -1;
                    modoAtualizado = false;
                    txtNome.setText("");
                    txtIdade.setText("");
                    Cadastrar.setText("Cadastrar");
                }
            }
        });
        
    }

