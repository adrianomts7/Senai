    private void atualizarTabela() {
        tabelaModelo.setRowCount(0);
        
        ArrayList<Pessoa> lista = controller.listar();
        
        for (Pessoa p : lista ) {
            tabelaModelo.addRow(new Object[]{p.getId(), p.getNome(), p.getIdade()});
        }
        
    }
    
    private void buscarDinamicamente() {
        String nomeBuscado = txtBuscar.getText().trim();
        
        tabelaModelo.setRowCount(0);
        ArrayList<Pessoa> lista = controller.buscarPorNome(nomeBuscado);
        
        for (Pessoa p : lista) {
            tabelaModelo.addRow(new Object[]{p.getId(), p.getNome(), p.getIdade()});
        }
        
    }
    
