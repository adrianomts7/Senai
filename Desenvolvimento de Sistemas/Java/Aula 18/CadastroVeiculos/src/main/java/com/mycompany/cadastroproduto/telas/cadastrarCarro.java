    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        try {
           int numPortas = Integer.parseInt(numPortasTxt.getText());
           String marca = marcaTxt.getText().trim();
           String modelo = modeloTxt.getText().trim();
           
           if (numPortas < 1 || numPortas > 4) {
               JOptionPane.showMessageDialog(null, "Digite o nÃºmero de portas validos!");
               return;
           } 
           
           if (marca.isEmpty() || modelo.isEmpty()) {
               JOptionPane.showMessageDialog(null, "Os campos nÃ£o podem ficar vazio");
               return;
           }
           
           Carro c = new Carro(numPortas, marca, modelo);
           
           if (listadeCarros.add(c)){
               JOptionPane.showMessageDialog(null, "Carro cadastrado com sucesso!");
           }
           
           numPortasTxt.setText("");
           marcaTxt.setText("");
           modeloTxt.setText("");
           
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite um nÃºmero de portas validos");
            return;
        }
        
        
        
    }//GEN-LAST:event_CadastrarActionPerformed

    private void ListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListarActionPerformed
        numPortasTxt.setText("");
        marcaTxt.setText("");
        modeloTxt.setText("");
        
        if (listadeCarros.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nenhum carro cadastrado");
            return;
        }
        
        for (Carro c : listadeCarros) {
            telaTx.append("\nMarca: " + c.getMarca() + "\nModelo: " + c.getModelo() + "\nQuantidade de Portas: " + c.getNumeroPorta() + "\n");
        }
       
    }//GEN-LAST:event_ListarActionPerformed

    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        try {
            JOptionPane.showMessageDialog(null, "Para editar de forma correta, Digite o modelo antigo e depois vocÃª digitarÃ¡ o nome do novo modelo do carro");
            
            String marca = marcaTxt.getText().trim();
            String modelo = modeloTxt.getText().trim();
            int numPortas = Integer.parseInt(numPortasTxt.getText());
            boolean alterouCarro = false;

            if (marca.isEmpty() || modelo.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Os campos nÃ£o podem estar vazio");
                return;
            }

            if (numPortas < 1 || numPortas > 4) {
                JOptionPane.showMessageDialog(null, "Digite o nÃºmero de portas validos!");
                return;
            }


            for(Carro c : listadeCarros) {
                if(c.getModelo().equalsIgnoreCase(modelo)) {
                    String novoModelo = JOptionPane.showInputDialog(null, "Digite o novo modelo do carro: ", JOptionPane.QUESTION_MESSAGE);

                    if (novoModelo.isEmpty()) {
                        JOptionPane.showMessageDialog(null, "Digite um modelo valido");
                        return;
                    }

                    c.setModelo(novoModelo);
                    c.setMarca(marca);
                    c.setNumeroPorta(numPortas);
                    alterouCarro = true;
                    break;
                }
            }

            if (alterouCarro) {
                modeloTxt.setText("");
                marcaTxt.setText("");
                numPortasTxt.setText("");
                telaTx.setText("");
                JOptionPane.showMessageDialog(null, "Dados do carro alterado com sucesso!");
                telaTx.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Os dados nÃ£o foram alterados!");
            }    
            
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "NÃºmero de portas invalidos");
        }
        
        
    }//GEN-LAST:event_EditarActionPerformed

