    private void RemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveActionPerformed
        String modelo = modeloTxt.getText().trim();
        boolean carroRemovido = false;
        
        for (int i = 0; i < listadeCarros.size(); i++ ){
            if(listadeCarros.get(i).getModelo().equalsIgnoreCase(modelo)){
                listadeCarros.remove(i);
                carroRemovido = true;
                break;
            }
        }
        
        modeloTxt.setText("");
        telaTx.setText("");
        if (carroRemovido) {
            JOptionPane.showMessageDialog(null, "Carro removido com sucesso!");
            
        } else {
            JOptionPane.showMessageDialog(null, "Carro nÃ£o foi removido");
        }
        
    }//GEN-LAST:event_RemoveActionPerformed

